const EventEmitter = require('events')
const net = require('net');
const PORT = 8088;
const TIMEOUT = 1500;
const STX = 0xEE;
const ETX = 0xFE;


module.exports = class DCS100 extends EventEmitter {
    constructor(ip_addr, debug = false) {
        super();
        this.IP = ip_addr;
        this.debug = debug;
        this.busy = true;
        this.mq = [];
        this.openMics = [];
        this.connection();        
    }

    log = function (mess) {
        if (this.debug) {
            let d = new Date();
            let time = d.getHours().toString().padStart(2, '0') + ":" + d.getMinutes().toString().padStart(2, '0') + ":" +
                d.getSeconds().toString().padStart(2, '0') + "." + d.getMilliseconds().toString().padStart(3, '0');
            console.log(`${time} DCS100 [${this.IP}] ${mess}`);
        }
    }

    hexstring = (hex) => {
        let s = "";
        for(let i = 0; i < hex.length; i++) {
            s += ((hex[i] & 0xF0) >> 4).toString(16).toUpperCase();
            s += ((hex[i] & 0x0F) >> 0).toString(16).toUpperCase();
            s += ' ';
        }
        return s;
    }

    connection = () => {
        this.socket = net.createConnection({ port: PORT, host: this.IP }, () => {
            this.log("[OK] connected");
            this.busy = false;
            this.sync();
            this.scan();
        });
        this.socket.on('data', (rx) => {
            this.log(`[OK] >--- ${this.hexstring(rx)}`);            
            this.parser(rx);
            this.sendNext();            
        });
        this.socket.on('end', () => {
            this.log("[OK] disconnected");
            this.reconnection();
        });
        this.socket.on('error', (err) => {
            this.log("[ERR] connection failed: " + err.message);
            this.reconnection();
        });
    };    

    reconnection = () => {
        this.socket.end();
        this.socket.removeAllListeners();
        setTimeout(this.connection, TIMEOUT);
    };

    // STX | addr | func | len | data | crc | ETX
    // ee 00 34 01 00 35 fe - off all mics
    // ee 06 31 01 01 39 fe - on 6 mic    
    send = (addr, func, data) => {
        let crc = addr + func + data.length
        data.forEach(byte => crc += byte)
        let tx = [STX, addr, func, data.length, data, crc&0xFF, ETX]
        this.mq.push(Buffer.from(tx.flat()));
        this.log(`[SEND] ---< ${this.hexstring(tx.flat())}`);    
        this.sending();
    }

    sending = () => {
        if (this.mq.length <= 0 || this.busy) { return; }
        this.busy = true;        
        let tx = this.mq.shift();
        try {
            this.socket.write(tx);
            this.log(`[OK] ---< ${this.hexstring(tx)}`);    
            this.sendNext();    
            this.watchdog = setTimeout(() => {
                if (this.busy) {
                    this.log('[ERR] No answer, watchdog.')                
                    this.sendNext();
                }
            }, 2000);
        } catch(e) {
            this.log(`[ERR] Socket Write: ${e.message}`);
            this.reconnection();
        }
    }

    sendNext = () => {
        clearTimeout(this.watchdog);
        setTimeout(() => {
            this.busy = false;
            this.sending();
        }, 10);
    }

    
    micOn = (mic) => this.send(mic, 0x31, [0x00]);            // Соответствующий микрофон включен
    micOff = (mic) => this.send(mic, 0x32, [0x00]);           // Соответствующий микрофон выключен
    toggle = (mic) => {
        if (this.openMics.includes(mic)) {
            this.micOff(mic);
        } else {
            this.micOn(mic);
        }
    }
    volume = (mic, vol) => this.send(mic, 0x33, [vol]);    // Соответствующий регулятор громкости микрофона: 0x01 = -6 dB, 0x02 = -3 dB, 0x03 = 0 dB, 0x04 = 3 dB, 0x05 = 6 dB 
    allOff = () => this.send(0x00, 0x34, [0x00]);          // Все микрофоны выключены
    setMode = (mode) => this.send(0x00, 0x36, [mode]);     // Переключение режима речи: 0x01=FIFO, 0x02=нормальный, 0x04=свободный, 0x08=приложение
    setOpenMics = (max) => this.send(0x00, 0x37, [max]);   // Изменено количество выступлений: 0x01 = 1, 0x02 = 2, 0x04 = 4, 0x08 = 6
    setHighEQ = (vol) => this.send(0x00, 0x38, [vol]);     // регулировка ВЧ: 0x01=-6 дБ, 0x02=-2 дБ, 0x03=0 дБ, 0x04=2 дБ, 0x05=6 дБ.
    setLowEQ = (vol) => this.send(0x00, 0x39, [vol]);      // регулировка НЧ: 0x01=-6 дБ, 0x02=-2 дБ, 0x03=0 дБ, 0x04=2 дБ, 0x05=6 дБ
    setVolume = (vol) => this.send(0x00, 0x3A, [vol]);     // Регулировка громкости: 0x01=-15 дБ, 0x02=-10 дБ, 0x03=-5 дБ, 0x04=-3 дБ, 0x05=0 дБ    

    sync = () => this.send(0x00, 0x3d, [0x00]);            // Запрос на синхронизацию данных организатора собрания
    scan = () => this.send(0x00, 0x3f, [0x00]);            // Начать сканирование подключенных микрофонов

    // -- не проверенные 
    delegateOff = (mic) => this.send(mic, 0x35, [0x00]);   // Сообщите хосту, чтобы он выключил микрофон репрезентативной машины
    open = (peoples) => this.send(0x00, 0x3B, [peoples]);  // Уведомить хост о включении входа, данные 1 — это количество людей, которые вошли в систему
    close = () => this.send(0x00, 0x3C, [0x00]);           // Уведомить хоста, чтобы закрыть вход
    activate = (mic) => this.send(mic, 0x3E, [0x00]);      // Согласитесь подать заявку на открытие микрофона
    vipOn = (mic) => this.send(mic, 0x40, [0x01]);         // Настройки пульта делегата VIP:
    vipOff = (mic) => this.send(mic, 0x40, [0x00]);        //    ID: идентификационный номер, данные 1: 0x01 вкл., 0x00 выкл

    // STX | addr | func | len | data | crc | ETX
    parser = (rx) => {                
//        this.log(`[PARSER] ::: ${this.hexstring(rx)}`);
        if (rx.length >= 7) {
            
            let stx = rx[0]
            let addr = rx[1]
            let func = rx[2]
            let len = rx[3]
            let data = rx.slice(4, -2)
            let crc = rx.slice(-2, -1)[0]
            let etx = rx.slice(-1)[0]

            if (stx !== STX || etx !== ETX) { 
                this.log(`[ERROR] ::: Invalid packet from DCS100. Skipping. STX/ETX error. ${this.hexstring([stx])}..${this.hexstring([etx])}`)
                return 
            }

            switch(func) {
                case 0x21:                          // Отправка списка микрофонов онлайн
                    this.parserMicOnline(data);
                    break;
                case 0x22:                          // Возврат данных синхронизации на ПК
                case 0x23:                          // Включить уведомление об идентификаторе конфигурации
                case 0x24:                          // Отключить уведомление об идентификаторе конфигурации
                case 0x25:                          // вывеска с микрофоном
                case 0x26:                          // Обратная связь управления микрофоном
                case 0x27:                          // Уведомление о переключении микрофона
                case 0x28:                          // Уведомление о применении микрофона
                case 0x29:                          // Обновить количество микрофонов
                case 0x2A:                          // Обновить режим микрофона
                case 0x2B:                          // обновить громкость басов
                case 0x2C:                          // обновить высокие частоты
                case 0x2D:                          // обновить общую громкость
                case 0x2E:                          // Обновить громкость микрофона
                    break;                
                default:
                    this.log('[ERROR] ::: Invalid packet func from DCS100. Skipping.')
            }            
        } else {
            this.log('[ERROR] ::: Invalid packet from DCS100. Skipping. Packet.length < 7')
        }
    }


    //  EE 00 21 50 
    //              6E 00 00 00 00 00 00 00 00 00 00 00 00 00 00 00                  - online/offline (16 byte x 8 bit = 128 mics)
    //              0E 00 00 00 00 00 00 00 00 00 00 00 00 00 00 00                  - on/off (16 byte x 8 bit = 128 mics)
    //              60 00 00 00 00 00 00 00 00 00 00 00 00 00 00 00                  - ?? (16 byte x 8 bit = 128 mics)
    //              00 00 00 00 00 00 00 00 00 00 00 00 00 00 00 00                  - ?? (16 byte x 8 bit = 128 mics)
    //              01 00 00 00 00 00 00 00 00 00 00 00 00 00 00 00                  - ?? (16 byte x 8 bit = 128 mics)
    //                                                              4E FE
    
    parserMicOnline = (data) => {
        let slice1 = data.slice(0, 16)           // mic online 
        let slice2 = data.slice(16, 32)          // mic on/off
        let slice3 = data.slice(32, 48)          // ???
        let slice4 = data.slice(48, 64)          // ???
        let slice5 = data.slice(64, 80)          // ???
        
        let onoff = this.bitsToBytes(slice2)

        this.openMics.forEach((mic, i) => {
            if (!onoff[mic-1]) {                
                // mic off 
                this.log(`[MIC OFF] Mic ${mic} off.`)
                this.openMics.splice(i, 1)
                this.emit('off', mic);                
                this.emitCams();        
            }
        });

        onoff.forEach((state, mic) => {
            if (state && !this.openMics.includes(mic+1)) {
                // mic on 
                this.openMics.push(mic+1)
                this.log(`[MIC ON] Mic ${mic+1} on.`)
                this.emit('on', mic+1);
                this.emitCams();
            }
        });        
    }

    // convert bits array to bytes array 
    // (16 bytes x 8 bits => 128 bytes)
    bitsToBytes = (bitArray) => {
        const byteResult = []

        for (let i = 0; i < bitArray.length; i++) {
            for (let j = 0; j < 8; j++) {                
                byteResult.push((bitArray[i] >>> j) & 0x01)
            }
        }        

        return byteResult;
    }

    // mic for camera
    emitCams = () => {
        this.emit('cams', this.openMics.length > 0 ? this.openMics[this.openMics.length - 1] : 0);
    }
};


